#include "ObstacleBody.h"

void ObstacleBody::SetZPos(float z) {
	zPos = z;
}

void ObstacleBody::SetYPos(float y) {
	yPos = y;
}

void ObstacleBody::SetXPos(float x) {
	xPos = x;
}

float ObstacleBody::GetZPos() {
	return zPos;
}

float ObstacleBody::GetYPos() {
	return yPos;
}

float ObstacleBody::GetXPos() {
	return xPos;
}